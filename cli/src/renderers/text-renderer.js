// ASCII text banner renderer with 4 fonts

const BLOCK_FONT = buildBlockFont();
const BANNER_FONT = buildBannerFont();
const MINI_FONT = buildMiniFont();
const SHADOW_FONT = buildShadowFont();

const FONTS = { block: BLOCK_FONT, banner: BANNER_FONT, mini: MINI_FONT, shadow: SHADOW_FONT };

export function renderText(text, options = {}) {
  const { font = 'block' } = options;
  const fontData = FONTS[font];
  if (!fontData) {
    return `Unknown font: ${font}. Available: block, banner, mini, shadow`;
  }

  const chars = text.toUpperCase().split('');
  const height = fontData[' '].length;
  const lines = Array.from({ length: height }, () => '');

  for (const ch of chars) {
    const glyph = fontData[ch] || fontData[' '];
    for (let row = 0; row < height; row++) {
      lines[row] += glyph[row];
    }
  }

  return lines.join('\n');
}

